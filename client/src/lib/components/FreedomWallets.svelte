<h4>Geldbeutel der Freiheit</h4>
<p><br /></p>
<h6>Schnellkurs Online Geldbeutel</h6>
<p><br /></p>

So wie wir einen Browser nutzen, um im Internet zu surfen, nutzen wir Online Wallets, um mit
Blockchains zu interagieren.
<p><br /></p>
Für kleine Geldmengen, sowie für die Interaktion mit Blockchains nutzen wir Online Wallets wie z.B.
<a href="https://metamask.io" target="_blank">metamask.io</a>
oder das im <a href="https://brave.com" target="_blank">brave.com</a> Browser eingebaute Wallet.
<p><br /></p>
Hier kann es das Problem geben, dass ausgediente totalitäre Strukturen sogar mitten in sogenannten Demokratien die Überwachung friedliebender und freiheitsliebender Menschen übertreiben.
<p><br></p>
Daher nutzen wir für die Kooperation von Mensch zu Mensch ohne übertriebene Überwachung 
im Hintergrund eher Offline Geldbeutel. <p><br></p>
<p><br /><br /></p>
<h6>Schnellkurs Offline Geldbeutel</h6>

<p><br /></p>
Eine der besten Möglichkeiten Freiheitsgeld aufzubewahren sind self hosted (nur Du hast den Privat Schlüssel),
cold (nicht mit dem Internet verbundene) Paperwallets.
<p><br /></p>
<a href="https://privacy-is-not-a-crime.de" target="_blank" data-svelte-h="svelte-mwxuuu">Privatsphäre ist kein Verbrechen</a>. 
<p><br></p>
Sie scheint eher einer der Garanten für Freiheit, Weltfrieden und Freundschaft zu sein. 
<p><br></p>

